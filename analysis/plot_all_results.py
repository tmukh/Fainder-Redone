# FIG 2 — Python vs Rust speedup across all 3 datasets × 2 modes
# ═══════════════════════════════════════════════════════════════════════════════
def fig2_rust_vs_python():
    # Corrected: both Python and Rust measured with suppress_results=True.
    # Rust uses its optimal thread count (t=4 for dev_small, t=16 for eval_medium).
    # eval_10gb omitted: Python baseline not yet measured with suppress_results.
    datasets = ["dev_small", "eval_medium"]
    labels   = ["dev\\_small\n(200q, 50k)\nt=4 optimal", "eval\\_medium\n(10kq, 200k)\nt=16 optimal"]

    speedup_reb = []
    for ds in datasets:
        d = baseline[ds]
        speedup_reb.append(d["Python rebinning"] / d["Rust rebinning"])

    x = np.arange(len(datasets))
    w = 0.45

    fig, ax = plt.subplots(figsize=(6, 4.2))
    bars = ax.bar(x, speedup_reb, w, color=RUST_COL, alpha=0.85,
                  label="Rust (optimal thread count)")

    for bar, sp, ds in zip(bars, speedup_reb, datasets):
        d = baseline[ds]
        ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.04,
                f"{sp:.2f}×\n({d['Python rebinning']:.2f}s→{d['Rust rebinning']:.2f}s)",
                ha="center", va="bottom", fontsize=8.5)

    ax.axhline(1.0, color="gray", lw=1.0, linestyle="--", label="No speedup (1×)")
    ax.set_xticks(x)
    ax.set_xticklabels(labels)
    ax.set_ylabel("Speedup (Python time / Rust time)")
    ax.set_title("Python → Rust query engine speedup\n"
                 "(suppress\\_results=True: set building excluded from both)")
    ax.legend()
    ax.set_ylim(0, 5.0)
    ax.grid(True, axis="y", alpha=0.3, linestyle="--")
    ax.grid(False, axis="x")

    # Annotation explaining t=1 is slower
    ax.annotate(
        "Rust t=1 is slower than Python\n(scalar vs. vectorized numpy);\nparallelism closes the gap",
        xy=(0, speedup_reb[0]), xytext=(0.35, 2.4),
        fontsize=8, color="gray",
        arrowprops=dict(arrowstyle="->", color="gray", lw=0.8),
    )

    fig.tight_layout()
    save(fig, "fig2_rust_vs_python_speedup")


